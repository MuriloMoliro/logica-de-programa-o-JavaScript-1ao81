// 1.	Faça um programa que solicite dois números inteiros e exiba a soma, subtração, multiplicação e divisão entre eles.

let valorUm = parseInt(prompt("Digite um valor: "))
let valorDois = parseInt(prompt("Digite outro valor: "))

let soma = valorUm + valorDois
let subtracao= valorUm - valorDois
let multiplicacao = valorUm * valorDois
let divisao = valorUm / valorDois

alert("A soma dos valores é de: " + soma)
alert("A subtração dos valores é de: " + subtracao)
alert("A multiplicação dos valores é de: " + multiplicacao)
alert("A divisão dos valores é de: " + divisao)