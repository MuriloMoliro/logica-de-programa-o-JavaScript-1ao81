// 2.	Escreva um programa que leia o raio de um círculo e calcule sua área e perímetro

let raio = parseFloat(prompt("Digite o valor da raio do círculo: "))

let area = 3.14 * raio^2
let perimetro = (2 * 3.14) * raio 

alert("A area do círculo é: " + area)
alert("O perímetro do círculo é: " + perimetro)